<script>
	function contact_submit() {
		var footer_name = $('#contact-name').val();
		var footer_email_address = $('#contact-email-id').val();
		var footer_membership = $('#contact-member').val();
		var footer_message = $('#contact-message').val();
		var number = /^[0-9-+]+$/;
		var popupmail = /^[\w\-\.\+]+\@[a-zA-Z0-9\.\-]+\.[a-zA-z0-9]{2,4}$/;

		if (footer_name == "") {
			$('#contact-name').focus();
			$('#contact-name').css({ "border": "1px solid red" });
			return false;

		}
		$('#contact-name').css({ "border": "1px solid green" });
		if (footer_email_address == "") {
			$('#contact-email-id').focus();
			$('#contact-email-id').css({ "border": "1px solid red" });
			return false;

		}
		if (!popupmail.test(footer_email_address)) {

			$('#contact-email-id').focus();
			$('#contact-email-id').css({ "border": "1px solid red" });
			return false;

		}
		$('#contact-email-id').css({ "border": "1px solid green" });
		if (footer_membership == "0") {
			$('#contact-member').focus();
			$('#contact-member').css({ "border": "1px solid red" });
			return false;

		}
		$('#contact-member').css({ "border": "1px solid green" });
		if (footer_message == "") {
			$('#contact-message').focus();
			$('#contact-message').css({ "border": "1px solid red" });
			return false;

		}
		$('#contact-message').css({ "border": "1px solid green" });
		$.ajax({
			url: "mail/product-mail.php",
			method: "POST",
			data: {
				name: footer_name,
				email_address: footer_email_address,
				membership: footer_membership,
				message: footer_message,
			},
			success: function (data) {
				$('#contact-name').val("");
				$('#contact-email-id').val("");
				$('#contact-member').val("");
				$('#contact-message').val("");
				$('#thank_you').html("Thanks for your interest with us, Our executives will get in touch with you shortly").css({ "color": "#000000" }).fadeIn(500).fadeOut(3000);
			}

		});
	}

</script>

