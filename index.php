	function contact_form_submit() {
		var footer_name = $('#banner-form-name').val();
		var footer_contact_no = $('#banner-form-contactno').val();
		var footer_email_address = $('#banner-form-email').val();
		var footer_subject = $('#banner-form-subject').val();
		var footer_ask_to_join = $('#banner-form-join').val();
		var number = /^[0-9-+]+$/;
		var popupmail = /^[\w\-\.\+]+\@[a-zA-Z0-9\.\-]+\.[a-zA-z0-9]{2,4}$/;

		if (footer_name == "") {
			$('#banner-form-name').focus();
			$('#banner-form-name').css({ "border": "1px solid red" });
			return false;
		}
		$('#banner-form-name').css({ "border": "1px solid green" });
		if (footer_contact_no == "") {

			$('#banner-form-contactno').focus();
			$('#banner-form-contactno').css({ "border": "1px solid red" });
			return false;
		}
		if (!number.test(footer_contact_no)) {

			$('#banner-form-contactno').focus();
			$('#banner-form-contactno').css({ "border": "1px solid red" });
			return false;
		}
		if ($('#banner-form-contactno').val().length < 10) {

			$('#banner-form-contactno').focus();
			$('#banner-form-contactno').css({ "border": "1px solid red" });
			return false;
		}
		$('#banner-form-contactno').css({ "border": "1px solid green" });
		if (footer_email_address == "") {
			$('#banner-form-email').focus();
			$('#banner-form-email').css({ "border": "1px solid red" });
			return false;

		}
		if (!popupmail.test(footer_email_address)) {

			$('#banner-form-email').focus();
			$('#banner-form-email').css({ "border": "1px solid red" });
			return false;
		}
		$('#banner-form-email').css({ "border": "1px solid green" });
		if (footer_subject == "") {
			$('#banner-form-subject').focus();
			$('#banner-form-subject').css({ "border": "1px solid red" });
			return false;
		}
		$('#banner-form-subject').css({ "border": "1px solid green" });
		if (footer_ask_to_join == "") {
			$('#banner-form-join').focus();
			$('#banner-form-join').css({ "border": "1px solid red" });
			return false;
		}
		$('#banner-form-join').css({ "border": "1px solid green" });
		$.ajax({
			url: "mail/contactus-mail.php",
			method: "POST",
			data: {
				name: footer_name,
				email_address: footer_email_address,
				contact_no: footer_contact_no,
				subject: footer_subject,
				ask_to_join: footer_ask_to_join,
			},
			success: function (data) {
				$('#banner-form-name').val("");
				$('#banner-form-phoneno').val("");
				$('#banner-form-email').val("");
				$('#banner-form-subject').val("");
				$('#banner-form-join').val("");
				$('#thank_you').html("Thanks for your interest with us, Our executives will get in touch with you shortly").css({ "color": "#000000" }).fadeIn(500).fadeOut(3000);
			}

		});
	}

</script>

